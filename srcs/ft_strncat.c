#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;

	i = 0;
	while (s1[i] != '\0')
		i = i + 1;
	j = 0;
	while (j < n && s2[j] != '\0')
	{
		s1[i + j] = s2[j];
		j = j + 1;
	}
	s1[i + j] = '\0';
	return (s1);
}
